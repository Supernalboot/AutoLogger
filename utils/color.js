module.exports = class Color {

	// Provides basic colors
	basic(color) {
		if (color === 'yellow') return '#F7FF32';
		if (color === 'purple') return '#B351F6';
		if (color === 'orange') return '#FF7027';
		if (color === 'green') return '#43D243';
		if (color === 'white') return '#FFFFFF';
		if (color === 'brown') return '#654321';
		if (color === 'black') return '#101010';
		if (color === 'blue') return '#3389FF';
		if (color === 'poke') return '#D65452';
		if (color === 'pink') return '#FD74C7';
		if (color === 'grey') return '#A9A9A9';
		if (color === 'red') return '#ff0000';
	}

};
